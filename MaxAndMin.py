#declaring numbers
num1 = float(input("Number1: "))
num2 = float(input("\nNumber 2: "))

#priting maximum of two numbers
Maximum = max(num1,num2)
print("Maximum Number between {0} and {1} is {2}".format(num1,num2,Maximum))

#printing minimum of two numbers
Minimum = min(num1,num2)
print("Minimum of {0} and {1} is {2}".format(num1,num2,Minimum))